int		ft_atoi(const char *str)
{
	int i;
	int res;
	int neg;

	i = 0;
	res = 0;
	neg = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9' && str[i] != '\0')
	{
		if (-1 * (res * 10 + str[i] - '0') == -2147483648)
			return (-2147483648);
		res = res * 10 + str[i] - '0';
		i++;
	}
	if (neg == 1)
		res = -res;
	return (res);
}
