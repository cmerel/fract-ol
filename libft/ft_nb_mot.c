#include "libft.h"

int		ft_nb_mot(char *str, char c)
{
	int i;
	int j;
	int k;

	i = 0;
	j = 0;
	k = 0;
	while (str[i])
	{
		while (str[i] != c && str[i] != '\0')
		{
			j = 0;
			i++;
		}
		if ((str[i] == c || str[i] == '\0') && j == 0)
		{
			k++;
			j = 1;
		}
		i++;
	}
	return (k);
}
